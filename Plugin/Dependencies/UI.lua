create = function(holder,parent)
	local object = Instance.new(holder.ClassName);
	object.Parent = parent;
	
	for property,value in pairs(holder) do
		if(type(value) == "table") then
			create(value,object);
		else
			if(property ~= "ClassName") then
				object[property] = value;
			end
		end
	end
	return object;
end

return function(holder)
	local main = create(objects,holder);
	script:Destroy();
	return main;
end